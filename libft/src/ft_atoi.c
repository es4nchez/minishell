#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	int		sign;
	int		rep;

	s = (char *)str;
	sign = 1;
	rep = 0;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s <= '9' && *s >= '0')
	{
		rep = (rep * 10) + (*s - '0');
		s++;
	}
	return (sign * rep);
}
